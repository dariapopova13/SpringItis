CREATE TABLE users(
  id BIGINT GENERATED BY DEFAULT AS IDENTITY(START
  WITH 1),
  name VARCHAR(20),
  age INT
);

CREATE TABLE auto (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  model VARCHAR(20)
);

CREATE TABLE users_auto(
  user_id BIGINT REFERENCES users(id),
  autos_id BIGINT REFERENCES auto(id)
);
